'use strict';

const data = ['hola', 2, 5, 'adios'];

// Determina cuál de los dos números que hay en el array es mayor. 
console.log('5 es mayor que 2', data[2]>data[1]);

// Determina el resultado de las cinco operaciones numéricas realizadas con los dos elementos numéricos. 
console.log('Suma', data[1]+data[2]);
console.log('Resta', data[1]-data[2]);
console.log('Multiplicación', data[1]*data[2]);
console.log('División', data[1]/data[2]);
console.log('Cociente', data[1]%data[2]);